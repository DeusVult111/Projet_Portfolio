      xhttp.onload = function() {
        if (this.status === 200) {
          alert("Mise à jour réussie");
        } else {
          alert("Erreur lors de la mise à jour");
        }
      };
      xhttp.send(`id=${id}&field=${field}&value=${encodeURIComponent(value)}`);
    }
  });
